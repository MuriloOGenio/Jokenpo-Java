    private void EscolhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EscolhaActionPerformed
        int jogador = 0;  
        String x =  JOptionPane.showInputDialog(null,"Escolha sua jogada\n 0-pedra\n1-tesoura\n2-papel");
        jogador = Integer.parseInt(x); 
        if(jogador == 0){
            pedra1.setVisible(true);
            papel1.setVisible(false);
            tesoura1.setVisible(false);
        }else if(jogador == 1){
            tesoura1.setVisible(true);
            pedra1.setVisible(false);
            papel1.setVisible(false);
        }else{
            papel1.setVisible(true);
             tesoura1.setVisible(false);
            pedra1.setVisible(false);
        }
        Random gerador = new Random();
        int pc = gerador.nextInt(3);
        if(pc == 0){
            pedra.setVisible(true);
            papel.setVisible(false);
            tesoura.setVisible(false);
        }else if(pc == 1){
            tesoura.setVisible(true);
            papel.setVisible(false);
            pedra.setVisible(false);
            
        }else{
            papel.setVisible(true);
            pedra.setVisible(false);
            tesoura.setVisible(false);
        }  
        if(jogador == 0 && pc == 1){
            contjog++;
        }else if(jogador == 1 && pc == 2){
            contjog++;
        }else if (jogador == 2 && pc == 0){
            contjog++;
        }
        
    }//GEN-LAST:event_EscolhaActionPerformed

